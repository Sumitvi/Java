public class Switch {
    public static void main(String[] args) {
        
        int number =5;
        switch (number) {
            case 2:
                System.out.println("Number is 2");
                break;
            case 5:
                System.out.println("Number is 5");
                break;
            case 3:
                System.out.println("Number is 3");
                break;
            default:
            System.out.println("Error");
                break;
        }
    }
    
}
