import java.util.*;

public class bit{
    public static void main(String args[]){

        // Bitwise AND
        System.out.println(5 & 6);

        // BitWise OR
        System.out.println(5 | 6);

        // BitWise XOR

        System.out.println(5 ^ 6);

        // Binary One's Complement
        System.out.println(~5 );


        // Binary Left Shift <<

        System.out.println(5<<2);


        // Binary Right Shift

        System.out.println(6>>1);

    }
}